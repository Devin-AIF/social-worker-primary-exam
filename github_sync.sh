#!/bin/bash

# ==============================================================================
# GitHub 自动同步脚本
# ==============================================================================

# 进入项目目录
cd "/Users/devin_aif/Downloads/抓取题目"

echo "开始同步到 GitHub..."

# 1. 添加所有更改
git add .

# 2. 生成提交信息（包含当前时间）
DATE=$(date "+%Y-%m-%d %H:%M:%S")
COMMIT_MSG="Auto-sync: ${DATE}"

# 3. 提交更改
# 如果没有更改，git commit 会返回非零值，我们这里检查一下
if git commit -m "$COMMIT_MSG"; then
    echo "本地提交成功: $COMMIT_MSG"
else
    echo "没有检测到需要提交的更改。"
fi

# 4. 推送到 GitHub
# 假设分支名为 main，您可以根据实际情况修改
echo "正在推送至远程仓库..."
if git push origin main; then
    echo "-------------------------------------------"
    echo "同步成功！代码已推送到 GitHub。"
    echo "-------------------------------------------"
else
    echo "-------------------------------------------"
    echo "错误：推送失败！"
    echo "请检查：1. 网络连接 2. GitHub 权限（是否配置了 SSH Key 或 Token）"
    echo "-------------------------------------------"
    exit 1
fi
